function insertItems(arr, row1, row2) {
  arr[0].forEach((item) => {
    let chord = row1.insertCell(cellNo);
    chord.innerHTML = arr[0][cellNo];
    cellNo++;
  });
  cellNo = 0;
  arr[1].forEach((item) => {
    let chord = row2.insertCell(cellNo);
    chord.innerHTML = arr[1][cellNo];
    cellNo++;
  });
}

function insertItemsMajor(arr, table) {
  table.deleteRow(1);
  table.deleteRow(1);
  table.deleteRow(1);
  var row1 = table.insertRow();
  var row2 = table.insertRow();
  var row3 = table.insertRow();

  let chord = row1.insertCell(0);
  chord.innerHTML = "I - IV - V";
  chord = row1.insertCell(1);
  chord.innerHTML = arr[0][0] + " - " + arr[0][3] + " - " + arr[0][4];

  chord = row2.insertCell(0);
  chord.innerHTML = "I - VI - IV - V";
  chord = row2.insertCell(1);
  chord.innerHTML =
    arr[0][0] + " - " + arr[0][5] + " - " + arr[0][3] + " - " + arr[0][4];

  chord = row3.insertCell(0);
  chord.innerHTML = "II - V - I";
  chord = row3.insertCell(1);
  chord.innerHTML = arr[0][1] + " - " + arr[0][4] + " - " + arr[0][0];
}

