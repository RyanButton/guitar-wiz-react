function populateTable(key) {
  const table = document.getElementById("major-table");
  table.deleteRow(1);
  table.deleteRow(1);
  let row1 = table.insertRow();
  let row2 = table.insertRow();
  cellNo = 0;

  insertItems(chordLookup[key], row1, row2);
}

function populateProgTable(key) {
  const table = document.getElementById("prog-table");

  key.charAt(key.length - 1) === "m"
    ? insertItemsMinor(chordLookup[key], table)
    : insertItemsMajor(chordLookup[key], table);
}
